
export default {

	/**
	 * size
	 * decides the width
	 * @type string (small|medium|large)
	 */
	size: 'medium',

	/**
	 * display
	 * decides the display type
	 * @type string (component|notification)
	 */
	display: 'component',

	/**
	 * backdrop
	 * show/hide backdrop
	 * @type boolean
	 */
	backdrop: true,

	/**
	 * closeBackdrop
	 * if true, allows users to close modal by clicking backdrop
	 * @type boolean
	 */
	closeBackdrop: true,

	/**
	 * closeEscape
	 * if true allows users to close modal by pressing 'esc'
	 * @type boolean
	 */
	closeEscape: true,

	/**
	 * animate
	 * whether to animate the modal
	 * @type boolean
	 */
	animate: true,

	/**
	 * animateDuration
	 * duration of animation
	 * @type int
	 */
	animateDuration: 400

};
